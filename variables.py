# Concatenation
age = 100
parentName = "Grace N"
print("Hello "+parentName+", you are "+str(age)+" years old.")
print(f"Hello {parentName}, you are {age} years old.")

mathematics = 89
chemistry = 67
physics = 78
print(f"Hello {parentName}, your average is {(mathematics+chemistry+physics)/3}.")




