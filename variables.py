# Name variables using any string characters, except special characters but underscores
x = 10
y = 20
z = x + y
print(z)

firstNumber = 90
secondNumber = 100
thirdNumber = firstNumber / secondNumber
print(thirdNumber)

fruitName = "Mango"
print(fruitName)

firstStudent, secondStudent, thirdStudent = "John", "Faith", "Mike"
